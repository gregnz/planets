    public static Dictionary<string, HardpointSpec> GetWeapons()
    {
        Dictionary<string, HardpointSpec> weapons = new Dictionary<string, HardpointSpec>();

        foreach (string[] entry in energyCsv)
        {
            HardpointSpec hardpointSpec = new Energy(entry);
            weapons.Add(hardpointSpec.name, hardpointSpec);
        }

        foreach (string[] entry in ballisticsCsv)
        {
            HardpointSpec hardpointSpec = new Ballistic(entry);
            weapons.Add(hardpointSpec.name, hardpointSpec);
        }

        foreach (string[] entry in missiles)
        {
